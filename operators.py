a=10
b=8

print(a + b)    #18
print(a - b)    # 2
print(a * b)    # 80
print(a / b)    # 1.25 division giving at least one decimal place
print(a // b)   # 1 integer division, rounded down towards minus infinity
print(a % b)    # 2 modulo: the remainder after integer division

print()

for i in range(1, a // b):
    print(i)



